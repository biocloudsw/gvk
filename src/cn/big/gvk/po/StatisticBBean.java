package cn.big.gvk.po;

public class StatisticBBean {
    private int stB;
    private int traitId;
    private int pubNum;
    private int stdNum;
    private int assNum;
    private int subTraitNum;

    public int getStB() {
        return stB;
    }

    public void setStB(int stB) {
        this.stB = stB;
    }

    public int getTraitId() {
        return traitId;
    }

    public void setTraitId(int traitId) {
        this.traitId = traitId;
    }

    public int getPubNum() {
        return pubNum;
    }

    public void setPubNum(int pubNum) {
        this.pubNum = pubNum;
    }

    public int getStdNum() {
        return stdNum;
    }

    public void setStdNum(int stdNum) {
        this.stdNum = stdNum;
    }

    public int getAssNum() {
        return assNum;
    }

    public void setAssNum(int assNum) {
        this.assNum = assNum;
    }

    public int getSubTraitNum() {
        return subTraitNum;
    }

    public void setSubTraitNum(int subTraitNum) {
        this.subTraitNum = subTraitNum;
    }
}
